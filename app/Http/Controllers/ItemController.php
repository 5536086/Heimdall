    public function store(Request $request)
    {
        //
        $validatedData = $request->validate([
            'title' => 'required|max:255',
            'url' => 'required',
        ]);

        $item = new Item;

        $item->title = $request->title;
        $item->colour = $request->colour;
        $item->url = $request->url;

        $item->save();

        return redirect()->route('items.index');
    }

